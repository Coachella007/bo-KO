function smallFormHandler(el) {
    $(el).find('.concrete-bt').click(function() {

        if (typeof $(el).find('.formFname').val() !== 'undefined') {

            var fName = $(el).find('.formFname');
            var fNameVal = $(el).find('.formFname').val();
            var fNameError = $(el).find('.formFnameError');

            var format = /[ !\d@#$%^&*()_+\-=\[\]{};':"\\|,.<>\/?]/;
            fNameError.text('');
            fNameError.hide();
            if (fNameVal == '') {
                fNameError.text(errorMessages.fNameEmpty);
                fNameError.show();
                valid = false;
            } else if (fNameVal.length < 2) {
                fNameError.text(errorMessages.fNameTooShort);
                fNameError.show();
                valid = false;
            } else if (format.test(fNameVal)) {
                fNameError.text(errorMessages.fNameInvalid);
                fNameError.show();
                valid = false;
            }
        }

        if (typeof $(el).find('.formEmail').val() !== 'undefined') {

            var email = $(el).find('.formEmail');
            var emailVal = $(el).find('.formEmail').val();
            var emailError = $(el).find('.formEmailError');

            emailError.text('');
            emailError.hide();

            if (emailVal == '') {
                emailError.text(errorMessages.emailEmpty);
                emailError.show();
                valid = false;
            } else if (!isEmail(emailVal)) {
                emailError.text(errorMessages.emailInvalid);
                emailError.show();
                valid = false;
            }
        }

        var urlQuery = window.location.search.substring(1);
        var urlOrigin = window.location.origin;
        var redirectLink = $(el).data('redirectType');

        var name = $(el).find('.formFname').val();
        var email = $(el).find('.formEmail').val();
        var page_lang = $('html').attr('lang');
        if (name != null && email != null && name.length > 2 && isEmail(email)) {
            var def_url = urlOrigin + '/' + redirectLink + '/' + page_lang + '?name=' + name + '&email=' + email + '&' + urlQuery;
            window.location.replace(def_url);
        }
    });

}

